	public static int[] TwoNumberSum(int[] array, int targetSum) {
		int temp1;
        int temp2;
        
        for (int i = 0; i < array.Length; i++)
        {
            temp1 = array[i];

            for (int x = 0; x < array.Length; x++)
            {
                if (i != x)
                {
                    temp2 = array[x];
                    if (temp1 + temp2 == targetSum)
                    {
                        int[] myReturnArr = {temp1, temp2};
                        //Console.WriteLine($"found:{temp1} + {temp2} = {targetSum}");
                        return myReturnArr;
                    }
                }
            }
        }
        
		return new int[0];
	}


